    private static byte gfMul(int a, int b) {
        int ret = 0;
        for (int i=0; i < 8; i++) {
            if ((b & 1) == 1) ret ^= a;
            boolean term = (a & 0x80) != 0;
            a = (a << 1) & 0xFF;
            if (term) a ^= 0xC3;
            b >>= 1;
        }
        return (byte) (ret & 0xFF);
    }

